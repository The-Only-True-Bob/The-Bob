package com.github.the_only_true_bob.the_bob.afisha;

public class EventPlace {

    private String afishaId;
    private String placeId;
    private String date;
    private String placeName;
    private String placeAddress;
    private String type;
    private String name;
    private String afishaUrl;
    private String afishaImgUrl;

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAfishaUrl() {
        return afishaUrl;
    }

    public void setAfishaUrl(String afishaUrl) {
        this.afishaUrl = afishaUrl;
    }

    public String getAfishaImgUrl() {
        return afishaImgUrl;
    }

    public void setAfishaImgUrl(String afishaImgUrl) {
        this.afishaImgUrl = afishaImgUrl;
    }

    public String getAfishaId() {
        return afishaId;
    }

    public void setAfishaId(String afishaId) {
        this.afishaId = afishaId;
    }

    public String getPlaceId() {
        return placeId;
    }

    public void setPlaceId(String placeId) {
        this.placeId = placeId;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getPlaceName() {
        return placeName;
    }

    public void setPlaceName(String placeName) {
        this.placeName = placeName;
    }

    public String getPlaceAddress() {
        return placeAddress;
    }

    public void setPlaceAddress(String placeAddress) {
        this.placeAddress = placeAddress;
    }
}
